#include<iostream>
using namespace std;
int main() {
    
    int a = 2/5;
    cout << a << endl;

    float f = 2.0/5;
    cout << f << endl;

    int x = 3;
    int y = 5;

    bool first = (x == y);
    cout << first << endl;

    bool second = (x > y);
    cout << second << endl;

    bool third = (x < y);
    cout << third << endl;

    bool fourth = (x <= y);
    cout << fourth << endl;

    bool fifth = (x >= y);
    cout << fifth << endl;

    bool sixth = (x != y);
    cout << sixth << endl;

}
