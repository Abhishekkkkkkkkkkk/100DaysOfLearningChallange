// Array Literal Notation

let arr = [1, 2, 3, 4, 5];
console.log(arr);

let arr1 = [1, 'Abhishek', true, {name : "Abhishek"}];
console.log(arr1);
console.log(typeof(arr1));

// Array Constructor 

let arr2 = new Array('apple', 'banana', 'orange');
console.log(arr2);
console.log(typeof(arr1));

// Accessing the Array 
console.log(arr[0]);
console.log(arr1[2]);
console.log(arr2[1]);
